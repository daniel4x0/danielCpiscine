int	ft_atoi(char *str)
{
	int	i;
	int	neg;
	int	num;

	neg = 1;
	num = 0;
	i = 0;
	while (str[i] >= 0 && str[i] <= 32)
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10;
		num += (str[i] - 48);
		i++;
	}
	num *= neg;
	return (num);
}
